#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	s_len;
	size_t	i;

	s_len = ft_strlen(s);
	i = 0;
	while (i < s_len)
	{
		write(fd, &s[i], 1);
		i++;
	}
}
